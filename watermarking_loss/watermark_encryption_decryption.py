    def keygen(self, size, x=0.01, r=3.915):
        """
        This function generates a key for the specified number of length.

        :param size: the number of keys to be generated.
        :param x: x is the initial condition.
        :param r: r is the controlled parameter.
        :return: Returs the generated keys.
        """
        key = []

        for i in range(size):
            x = r * x * (1 - x)  # Logistic map
            key.append(int((x * pow(10, 16) % 2)))

        return key


    def generate_2d_key(self, xdims=32, ydims=32, x=0.01, r=3.915):
        """
        This function generates a key which will be used for watermark encryption.

        :param xdims: x axis shape
        :param ydims: y axis shape
        :param x: x is the initial condition.
        :param r: r is the controlled parameter.
        :return: Returs the generated logistic chaotic key.
        """

        keys = np.asarray(self.keygen(xdims * ydims, x, r))

        return np.resize(keys, xdims * ydims).reshape(xdims, ydims)


    def encrypt_watermark(self, watermark_img: np.ndarray, encryption_key: np.ndarray):
        """
        This function does watermark encryption by performing an XOR operation.

        :param watermark_img: The watermark that should go through encryption.
        :param encryption_key: The encryption key.
        :return: Returs the encrypted watermark.
        """

        return np.bitwise_xor(watermark_img, encryption_key)


    def decrypt_watermark(self, encypted_img: np.ndarray, encryption_key: np.ndarray):
        """
        This function does watermark encryption by performing an XOR operation.

        :param watermark_img: The watermark image that has been ecrypted.
        :param encryption_key: The encryption key.
        :return: Returs the encrypted watermark.
        """

        return np.bitwise_xor(encypted_img.astype(int), encryption_key.astype(int))


    def watermark_image_encryption(self):
        """
        This function integrates and performs the encryption of watermark image.

        :return: Returns encrypted watermark image
        """

        # Generate a encryption key and save it
        if self.options.gen_enc_key:
            enc_key = self.generate_2d_key(32, 32)
            with open(self.options.enc_key_path, 'wb') as f:
                np.save(f, np.array(enc_key))

        watermark_img = self.get_watermark_img()  # Gets a 32*32 binary watermark logo

        with open(self.options.enc_key_path, 'rb') as f:
            enc_key = np.load(f)

        if self.options.is_enc_watermark:
            wat_enc = self.encrypt_watermark(watermark_img, enc_key)
            return wat_enc
        else:
            return watermark_img


    def watermark_image_decryption(self, encypted_img: np.ndarray):
        """
        This function integrates and performs the decryption of watermark image.

        :param encypted_img: the encrypted image.
        :return: Returns extracted watermark image
        """

        with open(self.options.enc_key_path, 'rb') as f:
            enc_key = np.load(f)

        if self.options.is_enc_watermark:
            original_watermark = self.decrypt_watermark(encypted_img, enc_key)
            return original_watermark
        else:
            return encypted_img
